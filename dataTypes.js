/*
Primitive Data Tpes
1) Number
2) String
3) Boolean
4) Undefined
5) Null
4) Symbole
7) BigInt
*/

// 1) Number : Floating point numbers -> used for decimal and integer
let myNumber = 45;
console.log('Printing Number :', myNumber);
console.log('Data Type :', typeof myNumber);

// 2 String
let myName="Yajindra";
console.log('Printing String :', myName);
console.log('Data Type :', typeof myName);

// 3) Boolean: true or fals
let ageAboveTen = true;
console.log('Printing Boolean :', ageAboveTen);
console.log('Data Type :', typeof ageAboveTen);

// 4) Undefined : value not assigned
let undefineMe;
console.log('Printing Undefined :', undefineMe);
console.log('Data Type :', typeof undefineMe);

// 5) Null : empty nothng - typeof is object
let iAmNull = null;
console.log('Printing Null :', iAmNull);
console.log('Data Type :', typeof iAmNull);
